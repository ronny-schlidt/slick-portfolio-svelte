<h2 id="m263">Webanalyse, Monitoring und Optimierung</h2>
<p>
	Die Webanalyse (auch als „Reichweitenmessung" bezeichnet) dient der Auswertung der Besucherströme
	unseres Onlineangebots und kann Verhalten, Interessen oder demografische Informationen zu den
	Besuchern, wie beispielsweise Alter oder Geschlecht, als pseudonyme Werte umfassen. Mithilfe der
	Reichweitenanalyse können wir zum Beispiel erkennen, zu welcher Zeit unser Onlineangebot oder
	dessen Funktionen beziehungsweise Inhalte am häufigsten genutzt werden, oder zur Wiederverwendung
	einladen. Ebenso ist es uns möglich, nachzuvollziehen, welche Bereiche der Optimierung bedürfen.
</p>
<p>
	Neben der Webanalyse können wir auch Testverfahren einsetzen, um etwa unterschiedliche Versionen
	unseres Onlineangebots oder seiner Bestandteile zu testen und zu optimieren.
</p>
<p>
	Sofern nachfolgend nicht anders angegeben, können zu diesen Zwecken Profile, also zu einem
	Nutzungsvorgang zusammengefasste Daten, angelegt und Informationen in einem Browser bzw. in einem
	Endgerät gespeichert und dann ausgelesen werden. Zu den erhobenen Angaben gehören insbesondere
	besuchte Websites und dort genutzte Elemente sowie technische Auskünfte, wie etwa der verwendete
	Browser, das benutzte Computersystem sowie Angaben zu Nutzungszeiten. Sofern sich Nutzer in die
	Erhebung ihrer Standortdaten uns gegenüber oder gegenüber den Anbietern der von uns eingesetzten
	Dienste einverstanden erklärt haben, ist auch die Verarbeitung von Standortdaten möglich.
</p>
<p>
	Darüber hinaus werden die IP-Adressen der Nutzer gespeichert. Jedoch nutzen wir ein
	IP-Masking-Verfahren (d. h. Pseudonymisierung durch Kürzung der IP-Adresse) zum Schutz der Nutzer.
	Generell werden die im Rahmen von Webanalyse, A/B-Testings und Optimierung keine Klardaten der
	Nutzer (wie z. B. E-Mail-Adressen oder Namen) gespeichert, sondern Pseudonyme. Das heißt, wir als
	auch die Anbieter der eingesetzten Software kennen nicht die tatsächliche Identität der Nutzer,
	sondern nur die zum Zweck der jeweiligen Verfahren in deren Profilen gespeicherten Angaben.
</p>
